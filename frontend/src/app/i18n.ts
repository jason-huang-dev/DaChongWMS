export type TranslationKey = keyof (typeof keyedMessages)["en"];
type TranslationParams = Record<string, string | number | null | undefined>;

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{\{(\w+)\}\}/g, (_match, key: string) => {
    const value = params[key];
    return value === undefined || value === null ? "" : String(value);
  });
}

export function resolveTranslation(locale: AppLocale, key: TranslationKey, params?: TranslationParams): string {
  const message = keyedMessages[locale][key] ?? keyedMessages.en[key];
  return interpolate(message, params);
}

export function translateText(locale: AppLocale, value: string): string {
  if (!value) {
    return value;
  }

  return rawMessages[locale][value] ?? value;
}
